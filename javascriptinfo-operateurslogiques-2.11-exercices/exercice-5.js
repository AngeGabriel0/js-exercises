/* Ecrivez une condition "if" pour vérifier que l’age est compris entre 14 et 90 ans inclus.

“Inclus” signifie que l’age peut atteindre les 14 ou 90 ans. */

let age = 15;
if (age >= 14 && age <= 90) {
    console.log("L'âge est compris entre 14 et 90 ans inclus");
}
else {
    console.log("L'âge n'est pas compris entre 14 et 90 ans inclus");
}

/* Raisonnement : Si l'âge est supérieur ou égal à 14 et inférieur ou égal à 90, alors le console.log retournera la valeur après le if. 
Sinon, ce sera le console.log de else qui sera retourné. */